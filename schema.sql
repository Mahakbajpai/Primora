-- ─────────────────────────────────────────────
-- USEFUL VIEWS (optional but helpful for reporting)
-- ─────────────────────────────────────────────

CREATE OR REPLACE VIEW v_po_summary AS
SELECT
    po.id,
    po.reference_no,
    v.name          AS vendor_name,
    v.contact       AS vendor_contact,
    po.total_amount,
    ROUND(po.total_amount / 1.05, 2)               AS subtotal,
    ROUND(po.total_amount - (po.total_amount/1.05), 2) AS tax_amount,
    po.status,
    po.created_at,
    COUNT(poi.id)   AS item_count
FROM purchase_orders po
JOIN vendors v        ON v.id = po.vendor_id
LEFT JOIN purchase_order_items poi ON poi.po_id = po.id
GROUP BY po.id, v.name, v.contact;

COMMENT ON VIEW v_po_summary IS 'Denormalized PO summary with vendor info and item counts';
